    public static function getCurrencies( $permitted = null )
    {
        $data     = [];
        $currency = [];
        if ( isset( $permitted ) ) {
            $allowed = explode( ',', $permitted );
        }

        if ( $permitted ) {
            foreach ( self::$data as $item ) {
                $currency['CurrencyCode'] = $item['CurrencyCode'];
                $currency['Currency']     = $item['Currency'];
                if ( in_array( $item['CurrencyCode'], $allowed ) ) {
                    $data[] = $currency;
                }
            }
        } else {
            foreach ( self::$data as $item ) {
                $currency['CurrencyCode'] = $item['CurrencyCode'];
                $currency['Currency']     = $item['Currency'];
                $data[]                   = $currency;
            }
        }
        $data = array_unique( array_values( $data ), SORT_REGULAR );
        asort( $data );
        return $data;
    }

    public static function getCountries()
    {
        $data    = [];
        $country = [];
        foreach ( self::$data as $item ) {
            $country['CountryCode'] = $item['CountryCode'];
            $country['Country']     = $item['Country'];
            $data[]                 = $country;
        }
        return array_unique( $data );
    }
